#include <stdlib.h>
/**
 * add - add two number
 * @a: number
 * @b: number 
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two numbers
 * @a: number
 * @b: number
 * Return: subtraction of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}
 /**
  * mul - multiplication of two numbers
  * @a: number
  * @b: number
  * Return: multiplication of a and b
  */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * mod - mudula of two numbers
 * @a: number
 * @b: number
 * Return: mudula
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * divi - diviaion of two numbers
 * @a: number
 * @b: number
 * Return: division of two numbers
 */
int divi(int a, int b)
{
	return (a / b);
}
